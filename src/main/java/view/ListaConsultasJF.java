    public void loadConsultas(){
        DefaultTableModel modelo = (DefaultTableModel) tblConsultas.getModel();
        modelo.setNumRows(0);
        for (Consulta obj: dao.listaConsultas()){
            Object[] linha = {
                obj,
                obj.getAnimal(),
                obj.getTutor()
            };
            modelo.addRow(linha);
        }
    }

