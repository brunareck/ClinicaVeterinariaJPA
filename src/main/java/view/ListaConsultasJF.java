    private void btnNovoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNovoActionPerformed
        CadastroConsultaJD telaConsulta = new CadastroConsultaJD(this, rootPaneCheckingEnabled);
        telaConsulta.setVisible(true);
        
        Consulta novoObj = telaConsulta.getConsulta();
        
        if(novoObj != null){
            try{
                dao.persist(novoObj);
                loadConsultas();
            }catch (Exception ex){
                System.err.println("Erro ao salvar nova Consulta" + novoObj + "\n Erro: " + ex);
            }
        }
    }//GEN-LAST:event_btnNovoActionPerformed

    private void btnRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoverActionPerformed
        if (tblConsultas.getSelectedRow()!= -1){
            Consulta obj = (Consulta) tblConsultas.getModel().getValueAt(tblConsultas.getSelectedRow(), 0);
            String txtConsulta = "Consulta: {animal" + obj.getAnimal().getNome() + ", tutor: "+obj.getTutor().getNome() + "veterinario: " + obj.getVeterinario().getNome() + "}";
            int op_remover = JOptionPane.showConfirmDialog(rootPane, "Tem certeza que deseja remover " + txtConsulta + "?");
            if(op_remover == JOptionPane.YES_OPTION){
                try{
                    dao.remover(obj);
                }catch (Exception ex){
                    System.err.println("Erro ao remover " + txtConsulta + "\n Erro: " + ex);
                }
                JOptionPane.showMessageDialog(rootPane, "Consulta removida com sucesso!");
                loadConsultas();
            }
        }else {
            JOptionPane.showMessageDialog(rootPane, "Selecione uma linha.");
        }
    }//GEN-LAST:event_btnRemoverActionPerformed

    private void btnInfoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInfoActionPerformed
        if(tblConsultas.getSelectedRow() != -1){
            Consulta obj = (Consulta) tblConsultas.getModel().getValueAt(tblConsultas.getSelectedRow(), 0);
            JOptionPane.showMessageDialog(rootPane, obj.exibirDados());
        }else {
            JOptionPane.showMessageDialog(rootPane, "Selecione uma linha.");
        }
    }//GEN-LAST:event_btnInfoActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        if (tblConsultas.getSelectedRow() != -1) {
            Consulta obj = (Consulta) tblConsultas.getModel().getValueAt(tblConsultas.getSelectedRow(), 0);
            CadastroConsultaJD telaConsulta = new CadastroConsultaJD(this, rootPaneCheckingEnabled);
            telaConsulta.setConsulta(obj);
            telaConsulta.setVisible(true);

            Consulta consultaEdt = telaConsulta.getConsulta();

            if (consultaEdt != null) {
                try {
                    dao.persist(consultaEdt);
                } catch (Exception ex) {
                    System.err.println("Erro ao editar Consulta.\n Erro: " + ex);
                }

                loadConsultas();
            }

        } else {
            JOptionPane.showMessageDialog(rootPane, "Selecione uma linha");
        }
    }//GEN-LAST:event_btnEditarActionPerformed

